package SatJune15thJava.Vehicle;

public class Vehicle {
    String make;
    String model;
    String year;
    String color;
    String type;

    public Vehicle(String make, String model, String year, String color, String type) {
        this.make = make;
        this.model = model;
        this.year = year;
        this.color = color;
        this.type = type;
    }

}
